import Link from "next/link";
import { getItemAggregates, getExtractedWorkflows } from "@/lib/backend-data";
import {
  MECHANISM_HIERARCHY_SECTIONS,
  getOrderedItemTypes,
  TECHNIQUE_HIERARCHY_SECTIONS,
} from "@/lib/item-hierarchy";
import {
  ITEM_TYPE_LABELS,
  MECHANISM_LABELS,
  TECHNIQUE_LABELS,
} from "@/lib/vocabularies";
import {
  MECHANISM_DESCRIPTIONS,
  TECHNIQUE_DESCRIPTIONS,
} from "@/lib/explanations";
import type { ItemType, ItemAggregateTypeBucket } from "@/lib/types";

function bucketMap(buckets: ItemAggregateTypeBucket[]): Record<string, number> {
  const map: Record<string, number> = {};
  for (const b of buckets) map[b.value] = b.count;
  return map;
}

export default async function Home() {
  const [aggregates, extractedWorkflows] = await Promise.all([
    getItemAggregates(),
    getExtractedWorkflows(),
  ]);

  const typeCounts = bucketMap(aggregates.by_item_type);
  const mechCounts = bucketMap(aggregates.by_mechanism);
  const techCounts = bucketMap(aggregates.by_technique);

  const mechanismKeys = aggregates.by_mechanism
    .map((b) => b.value)
    .sort((a, b) =>
      (MECHANISM_LABELS[a] ?? a).localeCompare(MECHANISM_LABELS[b] ?? b),
    );

  const techniqueKeys = aggregates.by_technique
    .map((b) => b.value)
    .sort((a, b) =>
      (TECHNIQUE_LABELS[a] ?? a).localeCompare(TECHNIQUE_LABELS[b] ?? b),
    );

  return (
    <div>
      {/* Masthead */}
      <header className="mb-16 pt-8">
        <div className="grid gap-8 lg:grid-cols-[minmax(0,0.9fr)_minmax(0,1.1fr)] lg:items-start">
          <blockquote className="order-2 border-l-2 border-accent/30 pl-5 text-[22px] leading-relaxed text-ink lg:order-1">
            <p className="whitespace-pre-line">
              {`I live my life in circles that grow wide
And endlessly unroll,
I may not reach the last, but on I glide
Strong pinioned toward my goal.

About the old tower, dark against the sky,
The beat of my wings hums,
I circle about God, sweep far and high
On through milleniums.

Am I a bird that skims the clouds along,
Or am I a wild storm, or a great song?`}
            </p>
            <p className="mt-3 font-ui text-sm tracking-wide text-ink-muted not-italic">
              Rainer Maria Rilke,{" "}
              <span className="italic">The Book of a Monk&apos;s Life</span>
            </p>
          </blockquote>

          <div className="order-1 lg:order-2">
            <p className="small-caps mb-4 text-accent">
              Evidence-first engineering knowledge system
            </p>
            <p className="max-w-2xl text-lg leading-relaxed text-ink-secondary">
              A structured reference book for biological engineering. This guide
              covers the mechanisms, architectures, components, methods, and
              design&#x2013;build&#x2013;test&#x2013;learn workflows. It is
              generated by AI combing the (mostly) human literature, and
              curating the db into something useful. There is an API service
              available for programmatic access. This is meant to serve as an
              inspiration source or workbook for engineering complex biological
              systems.
            </p>
          </div>
        </div>

        {/* Stats as a quiet data line */}
        <div className="mt-8 flex flex-wrap gap-x-8 gap-y-2 font-data text-sm tracking-wide text-ink-muted">
          <span>
            <strong className="text-ink">{aggregates.total_items}</strong> items
          </span>
          <span>
            <strong className="text-ink">{aggregates.total_families}</strong> families
          </span>
          <span>
            <strong className="text-ink">{extractedWorkflows.length}</strong> workflows
          </span>
          {aggregates.avg_evidence_score !== null && (
            <span>
              <strong className="text-ink">
                {Math.round(aggregates.avg_evidence_score * 100)}
              </strong>{" "}
              avg evidence score
            </span>
          )}
        </div>
      </header>

      <hr className="mb-16" />

      {/* Toolkit model */}
      <section className="mb-16">
        <p className="small-caps mb-4">Toolkit Model</p>
        <p className="mb-8 max-w-3xl text-[15px] leading-relaxed text-ink-secondary">
          The collection is best read as two hierarchies beneath workflows:
          mechanisms and techniques. Within mechanisms, the hierarchy runs from
          mechanism to architecture to component. Within techniques, the
          hierarchy runs from technique to method.
        </p>
        <div className="grid gap-8 lg:grid-cols-2">
          <div className="border border-edge p-5">
            <p className="small-caps mb-3 text-accent">Mechanism Branch</p>
            <div className="space-y-4">
              {MECHANISM_HIERARCHY_SECTIONS.map((section, index) => (
                <details
                  key={section.id}
                  className="group/layer"
                >
                  <summary className="cursor-pointer list-none [&::-webkit-details-marker]:hidden">
                    <div className="flex items-center justify-between">
                      <div>
                        <p className="small-caps mb-1 text-ink-muted">
                          Layer {index + 1}
                        </p>
                        <h3>{section.title}</h3>
                      </div>
                      <span className="text-ink-muted transition-transform duration-200 group-open/layer:rotate-180">
                        ▾
                      </span>
                    </div>
                    <p className="mt-1 text-sm leading-relaxed text-ink-secondary">
                      {section.description}
                    </p>
                  </summary>
                  <div className="mt-3">
                    {section.id === "mechanism" ? (
                      <div className="grid gap-3 md:grid-cols-2">
                        {mechanismKeys.map((key) => {
                          const count = mechCounts[key] ?? 0;
                          const description =
                            MECHANISM_DESCRIPTIONS[key] ??
                            "A mechanism-level grouping derived from the current toolkit evidence.";
                          return (
                            <Link
                              key={key}
                              href={`/mechanisms/${encodeURIComponent(key)}`}
                              className="group rounded border border-edge p-4 transition-colors hover:border-accent"
                            >
                              <div className="flex items-baseline justify-between gap-3">
                                <span className="font-display text-lg text-ink group-hover:text-accent">
                                  {MECHANISM_LABELS[key] ?? key.replace(/_/g, " ")}
                                </span>
                                <span className="font-data text-xs text-ink-muted">
                                  {count}
                                </span>
                              </div>
                              <p className="mt-2 text-sm leading-relaxed text-ink-secondary">
                                {description}
                              </p>
                            </Link>
                          );
                        })}
                      </div>
                    ) : (
                      <div className="space-y-3">
                        {getOrderedItemTypes(section.itemTypes ?? [])
                          .filter((type) => (typeCounts[type] ?? 0) > 0)
                          .map((type) => (
                            <Link
                              key={type}
                              href={`/items?type=${type}`}
                              className="group flex items-baseline justify-between border-b border-edge py-2 transition-colors hover:border-accent"
                            >
                              <span className="font-display text-lg text-ink group-hover:text-accent">
                                {ITEM_TYPE_LABELS[type as ItemType]}
                              </span>
                              <span className="font-data text-sm tabular-nums text-ink-muted">
                                {typeCounts[type] ?? 0}
                              </span>
                            </Link>
                          ))}
                      </div>
                    )}
                  </div>
                </details>
              ))}
            </div>
          </div>

          <div className="border border-edge p-5">
            <p className="small-caps mb-3 text-accent">Technique Branch</p>
            <div className="space-y-4">
              {TECHNIQUE_HIERARCHY_SECTIONS.map((section, index) => (
                <details
                  key={section.id}
                  className="group/layer"
                >
                  <summary className="cursor-pointer list-none [&::-webkit-details-marker]:hidden">
                    <div className="flex items-center justify-between">
                      <div>
                        <p className="small-caps mb-1 text-ink-muted">
                          Layer {index + 1}
                        </p>
                        <h3>{section.title}</h3>
                      </div>
                      <span className="text-ink-muted transition-transform duration-200 group-open/layer:rotate-180">
                        ▾
                      </span>
                    </div>
                    <p className="mt-1 text-sm leading-relaxed text-ink-secondary">
                      {section.description}
                    </p>
                  </summary>
                  <div className="mt-3">
                    {section.id === "technique" ? (
                      <div className="grid gap-3 md:grid-cols-2">
                        {techniqueKeys.map((key) => {
                          const count = techCounts[key] ?? 0;
                          const description =
                            TECHNIQUE_DESCRIPTIONS[key] ??
                            "A technique-level grouping derived from the current toolkit evidence.";
                          return (
                            <Link
                              key={key}
                              href={`/techniques/${encodeURIComponent(key)}`}
                              className="group rounded border border-edge p-4 transition-colors hover:border-accent"
                            >
                              <div className="flex items-baseline justify-between gap-3">
                                <span className="font-display text-lg text-ink group-hover:text-accent">
                                  {TECHNIQUE_LABELS[key] ?? key.replace(/_/g, " ")}
                                </span>
                                <span className="font-data text-xs text-ink-muted">
                                  {count}
                                </span>
                              </div>
                              <p className="mt-2 text-sm leading-relaxed text-ink-secondary">
                                {description}
                              </p>
                            </Link>
                          );
                        })}
                      </div>
                    ) : (
                      <div className="space-y-3">
                        {getOrderedItemTypes(section.itemTypes ?? [])
                          .filter((type) => (typeCounts[type] ?? 0) > 0)
                          .map((type) => (
                            <Link
                              key={type}
                              href={`/items?type=${type}`}
                              className="group flex items-baseline justify-between border-b border-edge py-2 transition-colors hover:border-accent"
                            >
                              <span className="font-display text-lg text-ink group-hover:text-accent">
                                {ITEM_TYPE_LABELS[type as ItemType]}
                              </span>
                              <span className="font-data text-sm tabular-nums text-ink-muted">
                                {typeCounts[type] ?? 0}
                              </span>
                            </Link>
                          ))}
                      </div>
                    )}
                  </div>
                </details>
              ))}
            </div>
          </div>
        </div>
        {aggregates.by_family.length > 0 && (
          <>
            <p className="mt-4 max-w-3xl text-sm leading-relaxed text-ink-secondary">
              Families group related lineages that share evolutionary or engineering
              ancestry. They complement the mechanism and technique hierarchies above.
            </p>
            <div className="mt-4 flex flex-wrap gap-x-6 gap-y-2">
              {aggregates.by_family.map((f) => (
                <Link
                  key={f.value}
                  href={`/items?family=${encodeURIComponent(f.value)}`}
                  className="group font-body text-base text-ink-secondary transition-colors hover:text-accent"
                >
                  {f.value}
                  <sup className="ml-0.5 font-data text-xs text-ink-muted group-hover:text-accent">
                    {f.count}
                  </sup>
                </Link>
              ))}
            </div>
          </>
        )}
        <p className="mt-6">
          <Link
            href="/items"
            className="small-caps text-accent hover:text-accent-hover"
          >
            Browse toolkit items &rarr;
          </Link>
        </p>
      </section>

      <hr className="accent mb-16" />

      {/* Workflows */}
      <section className="mb-8">
        <p className="small-caps mb-4">Workflow Layer</p>
        <p className="mb-6 max-w-3xl text-[15px] leading-relaxed text-ink-secondary">
          Workflows sit above both branches. They combine mechanisms and
          techniques to obtain, optimize, verify, characterize, deliver, and
          evaluate engineered systems. These are extracted from the primary
          literature.
        </p>
        {extractedWorkflows.length > 0 ? (
          <div>
            {extractedWorkflows.slice(0, 8).map((wf) => {
              const doc = wf.source_document;
              return (
                <Link
                  key={wf.workflow_id}
                  href="/workflows"
                  className="group block border-b border-edge py-5 transition-colors hover:border-accent"
                >
                  <h3 className="mb-1 text-ink group-hover:text-accent">
                    {wf.workflow_objective ?? "Untitled workflow"}
                  </h3>
                  {wf.why_workflow_works && (
                    <p className="mb-2 line-clamp-2 text-[15px] leading-relaxed text-ink-secondary">
                      {wf.why_workflow_works}
                    </p>
                  )}
                  <div className="flex flex-wrap items-center gap-x-3 gap-y-1 font-ui text-xs">
                    {doc?.title && (
                      <span className="text-ink-muted">{doc.title}</span>
                    )}
                    {doc?.publication_year && (
                      <span className="font-data text-ink-muted">
                        {doc.publication_year}
                      </span>
                    )}
                    <span className="font-data text-ink-muted">
                      {wf.stages.length > 0 &&
                        `${wf.stages.length} stages`}
                      {wf.stages.length > 0 && wf.steps.length > 0 &&
                        " · "}
                      {wf.steps.length > 0 &&
                        `${wf.steps.length} steps`}
                      {wf.involved_items.length > 0 &&
                        ` · ${wf.involved_items.length} tools`}
                    </span>
                  </div>
                </Link>
              );
            })}
            {extractedWorkflows.length > 8 && (
              <p className="mt-4 font-data text-sm text-ink-muted">
                + {extractedWorkflows.length - 8} more workflows
              </p>
            )}
          </div>
        ) : (
          <p className="text-ink-muted">
            No workflows extracted yet.
          </p>
        )}
        <p className="mt-6">
          <Link
            href="/workflows"
            className="small-caps text-accent hover:text-accent-hover"
          >
            Browse all workflows &rarr;
          </Link>
        </p>
      </section>
    </div>
  );
}
